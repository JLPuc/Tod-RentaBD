IF EXISTS (SELECT * FROM sys.all_objects WHERE object_id = OBJECT_ID('FILTRO') AND type IN ('U'))
	DROP TABLE FILTRO
GO

CREATE TABLE FILTRO(
	FiltroID            INT				NOT NULL,
	Palabra             VARCHAR(20)		NULL,
	CONSTRAINT PK_FILTRO_1 PRIMARY KEY NONCLUSTERED (FiltroID)
)
GO
	CREATE INDEX INDEX_FILTRO_1 ON FILTRO(FiltroID ASC)
GO